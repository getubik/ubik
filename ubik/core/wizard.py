# ── Answers + rendering ──────────────────────────────────────────────────


@dataclass(slots=True)
class WizardAnswers:
    project_name: str
    repo_path: str
    default_branch: str
    preset: ProviderPreset
    bridge_token_env: str = "TELEGRAM_BOT_TOKEN"
    bridge_chat_id_env: str = "TELEGRAM_CHAT_ID"
    approver_chat_ids: list[int] = field(default_factory=list)
    github_repo: str = ""  # e.g. "getubik/ubik"
    test_command: str | None = None
    max_proposals_per_day: int = 20
    daily_usd_cap: float = 15.0
    notebook_path: str = "./research"
    daily_at: str = "09:00"


def render_yaml(a: WizardAnswers) -> str:
    """Produce a fully-honest ubik.yaml — every field round-trips through the loader."""
    base_url_line = (
        f'    base_url: "{a.preset.base_url}"\n'
        if a.preset.base_url
        else "    base_url: null\n"
    )
    test_line = (
        f'  test_command: "{a.test_command}"\n' if a.test_command else "  test_command: null\n"
    )
    approvers = (
        "\n".join(f"    - {cid}" for cid in a.approver_chat_ids)
        or "    # - 123456789  # add your Telegram chat id(s) here"
    )
