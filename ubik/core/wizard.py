def render_env_example(a: WizardAnswers) -> str:
    """Companion .env.example — the secrets ubik.yaml refers to by env-var name."""
    lines = [
        "# Generated by `ubik init`. Copy to `.env` and fill in the values.",
        "# `.env` is in .gitignore by default; never commit secrets.",
        "",
        f"# LLM provider: {a.preset.label}",
        f"{a.preset.api_key_env}=",
        "",
        "# Telegram bridge — create a bot via @BotFather, then send it /start",
        "# from your account and visit https://api.telegram.org/bot<TOKEN>/getUpdates",
        "# to find your chat id.",
        f"{a.bridge_token_env}=",
        f"{a.bridge_chat_id_env}=",
        "",
        "# GitHub — verifier needs either `gh` on PATH (preferred) OR this token.",
        "GITHUB_TOKEN=",
    ]
    return "\n".join(lines) + "\n"


# ── Interactive flow ─────────────────────────────────────────────────────


def autodetect_default_branch(repo: Path) -> str:
    """Read git's HEAD reference; fall back to 'main'."""
    try:
        out = subprocess.run(
            ["git", "symbolic-ref", "refs/remotes/origin/HEAD", "--short"],
            cwd=str(repo), capture_output=True, text=True, timeout=3, check=False,
        )
        if out.returncode == 0:
            ref = out.stdout.strip()
            if "/" in ref:
                return ref.split("/", 1)[1]
    except (OSError, subprocess.SubprocessError):
        pass
    return "main"


_REPO_SLUG_RE = re.compile(r"github\.com[:/](?P<slug>[^/]+/[^/.]+)(?:\.git)?$")


def autodetect_github_slug(repo: Path) -> str:
    """origin URL → 'org/repo' or empty string."""
    try:
        out = subprocess.run(
            ["git", "remote", "get-url", "origin"],
            cwd=str(repo), capture_output=True, text=True, timeout=3, check=False,
        )
    except (OSError, subprocess.SubprocessError):
        return ""
    if out.returncode != 0:
        return ""
    m = _REPO_SLUG_RE.search((out.stdout or "").strip())
    return m.group("slug") if m else ""


def interactive_wizard(*, default_repo: Path, console: Console) -> WizardAnswers:
    """Prompt the user; return validated answers. Side-effect: prints to console."""
    console.print()
    console.print("[bold]🤫 Ubik · first-run wizard[/bold]")
    console.print(
        "[dim]Six questions, all with sensible defaults. "
        "You can edit ubik.yaml afterwards.[/dim]\n"
    )

    repo = default_repo.resolve()
    project_name = Prompt.ask("Project name", default=repo.name)
    repo_path = Prompt.ask(
        "Repo path Ubik should watch", default=str(repo)
    )
    default_branch = Prompt.ask(
        "Default branch", default=autodetect_default_branch(repo)
    )

    # LLM provider
    console.print("\n[bold]LLM provider[/bold]")
    for i, p in enumerate(PRESETS, 1):
        console.print(f"  [cyan]{i}[/cyan]. {p.label}")
    idx = IntPrompt.ask(
        "Choose", default=1, choices=[str(i) for i in range(1, len(PRESETS) + 1)]
    )
    preset = PRESETS[idx - 1]

    # Bridge
    console.print("\n[bold]Telegram bridge[/bold] [dim](only bridge in 0.1.0)[/dim]")
    chat_ids_raw = Prompt.ask(
        "Approver chat id(s), comma-separated [dim](leave blank to fill in later)[/dim]",
        default="",
    )
    approver_chat_ids = [
        int(x) for x in (s.strip() for s in chat_ids_raw.split(",")) if x.isdigit()
    ]

    # Verifier — GitHub
    console.print("\n[bold]GitHub verifier[/bold]")
    github_repo = Prompt.ask(
        "Repo slug (org/repo) for PRs", default=autodetect_github_slug(repo)
    )
    test_command = Prompt.ask(
        "Test command [dim](blank = skip tests during executor run)[/dim]",
        default="pytest -q",
    )

    # Cost caps
    console.print("\n[bold]Spending guards[/bold]")
    max_proposals = IntPrompt.ask("Max proposals per day", default=20)
    daily_usd = float(Prompt.ask("Daily USD cap (informational)", default="15"))

    daily_at = Prompt.ask("Daily audit time (HH:MM, local)", default="09:00")

    return WizardAnswers(
        project_name=project_name,
        repo_path=repo_path,
        default_branch=default_branch,
        preset=preset,
        approver_chat_ids=approver_chat_ids,
        github_repo=github_repo,
        test_command=test_command or None,
        max_proposals_per_day=max_proposals,
        daily_usd_cap=daily_usd,
        notebook_path="./research",
        daily_at=daily_at,
    )
